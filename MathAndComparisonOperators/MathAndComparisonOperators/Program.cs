using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MathAndComparisonOperators
{
    internal class Program
    {
        static void Main()
        {
            //Basic Addition operator is used for adding numbers
            int number1 = 15 + 10;
            Console.WriteLine("Fifteen plus ten: " + number1.ToString());
            Console.ReadLine();


            //Combined addition
            int number2 = 5 + 5;
            int number3 = 10 + 20;
            int total1 = number1 + number2;
            Console.WriteLine(total1);
            Console.ReadLine();



            //subtraction
            int num5 = 20 - 15;
            Console.WriteLine("Twenty minus fifteen: " + num5.ToString());
            Console.ReadLine();



            //Multiplication
            int num6 = 10 - 60;
            Console.WriteLine(num6);
            Console.ReadLine();


            //Division
            int num7 = 100 / 5;
            Console.WriteLine(num7);
            Console.ReadLine();



            //double data type in division
            double  num8 = 20.54 / 20.54;
            Console.WriteLine(num8);
            Console.ReadLine();


            //Modulus operator
            int num9 = 50 % 9;
            Console.WriteLine(num9);
            Console.ReadLine();


















        }
    }
}
